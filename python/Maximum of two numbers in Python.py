# Program to find the maximum of two numbers

# Input two numbers from the user
num1 = float(input("Enter the first number: "))  # First number input
num2 = float(input("Enter the second number: "))  # Second number input

# Find the maximum of the two numbers using the max function
maximum = max(num1, num2)

# Display the result
print("The maximum of", num1, "and", num2, "is", maximum)
